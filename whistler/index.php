<h2>History and scope of the collection</h2>
<p>
James Abbott McNeill Whistler was perhaps the most important and influential American artist of the 19th century, and has been a leading focus of my life and career. It is believed that more books by and about Whistler have been published than any other American artist (Andy Warhol, strangely, appears to be catching up).  
<br /><br />
I have been a collector of Whistler printed material (catalogues raisonn&eacute;s, dealer catalogues, museum exhibition catalogues, newspaper and magazine articles, biographies, Whistler&apos;s personally printed books, posters, cartoons, etc.) for about forty years.  This is believed by other Whistlerians to be the largest privately owned collection of its kind.  One inspiration to build this thematic collection is the presence in Detroit (a few miles from my gallery) of the Charles L. Freer House, former home of the preeminent Whistler collector and enthusiast, which is being revitalized at this time.
<br /><br />
Starting with a deluxe volume biography by Joseph Pennell, my collection turned obsessive, particularly after the acquisition of an extraordinary collection from a bookstore in Ohio in 1974.  Subsequent sources of materials have included French, American and British galleries and bookstores.
<br /><br />
Categories featured in my collection include Whistler as illustrator, Whistler as author, Whistler-related prints, Joseph Pennell, etc.  Among the rare highlights in the collection are privately printed books, books in French and other languages, the 57 Defaced Etchings, prints of Whistler by other artists (notably Sir William Nicholson&apos;s woodcut), Freer Gallery catalogues, books signed or annotated by authors including Whistler, Coast Survey etching of 1854, signatures of Whistler and collectors, and original Whistler prints in books.   Several important items have been loaned for museum exhibits and I have given lectures pertaining to Whistler&apos;s life and art at the Minneapolis Institute of Arts, Detroit Institute of Arts, Freer House, Oakland University, etc.
<br /><br />
If you are interested in seeing text or images of any of my material, I will do my best to accommodate your request.
<br />

<h2>Profile of Arnold Klein</h2>
<p>
Born and educated in New York City, I was first struck by the beauty and power of art seeing a William Zorach sculpture at the Radio City Music Hall, and cultivated a passion to appreciate, and collect, art.  I graduated from Hamilton College (B.A. in art and English, 1950) and Columbia University (M.A. in French, 1957), and attended the American School of Art and the University of Geneva, and taught French at Berkshire School, St. Paul&apos;s School and the University of Detroit.  From 1966-1971 I was Assistant Curator in the Graphic Arts Department at the Detroit Institute of Arts.  I founded the Arnold Klein Gallery (www.arnoldkleingallery.com) in 1971 with my wife, the artist Karen Anne Klein (www.kaklein.com) and have three children, Korinthia, Arno, and Barrett, who make and play instruments (www.korinthianviolins.com), image human brains (www.binarybottle.com), and pursue entomology and art (www.pupating.org), respectively.  The Arnold Klein Gallery specializes in American and West European original prints and drawings, 19th century to the present.
</p>
